int sum(int a, int b)
{
	return (a+b);
}

int divi(int a, int b)
{
	return (a/b);
}

int mod(int a, int b)
{
	return (a%b);
}

int diff(int a, int b)
{
	return (a-b);
}

int mult(int a, int b)
{
	return (a*b);
}